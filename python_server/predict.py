def reformat_arr(arr):  #i will get a 14*14 array, have to convert to 28 * 28
    arr = np.kron(arr, np.ones((2, 2)))
    arr = np.expand_dims(
        arr, axis=0
    )  # adds one more dimension, where this will be the [0]-th element. Becouse predict function works with more data sample.
    return arr


def predict(arr):
    model = load_model("digit_model.h5")
    label = model.predict(arr)
    label_a = label[0]
    return np.argmax(label_a)


